    public Integer matchingColorName(int pixelColor) {
        // largest difference is 255 for every colour component
        int currentDifference = 3 * 255;
        // Get the best  color's ID
        Integer closestColorID = null;
        // get int values for all three colour components of the pixel
        int pixelColorR = Color.red(pixelColor);
        int pixelColorG = Color.green(pixelColor);
        int pixelColorB = Color.blue(pixelColor);

        //For each color in map, try to compare the difference of received color
        //If different = 0, exact match is found.
        Iterator<Integer> colorNameIterator = mColors.keySet().iterator();
        while (colorNameIterator.hasNext() && currentDifference > 0) {
            // this colour's name
            Integer currentColorID = colorNameIterator.next();
            // this colour's int value
            int color = mColors.get(currentColorID);
            // get int values for all three colour components of this colour
            int colorR = Color.red(color);
            int colorG = Color.green(color);
            int colorB = Color.blue(color);
            // calculate sum of absolute differences that indicates how good this match is
            int difference = Math.abs(pixelColorR - colorR) + Math.abs(pixelColorG - colorG) + Math.abs(pixelColorB - colorB);
            // a smaller difference means a better match, so keep track of it
            if (currentDifference > difference) {
                currentDifference = difference;
                closestColorID = currentColorID;
            }
        }
        return closestColorID;
    }

    @NonNull
    public String getEnglishString(Context current,Integer colorID) {
        return getLocalizedResources(current,Locale.ENGLISH).getString(colorID);
    }

    @NonNull
    public String getChineseString(Context current,Integer colorID) {
        return getLocalizedResources(current,Locale.TRADITIONAL_CHINESE).getString(colorID);
    }

    @NonNull
    Resources getLocalizedResources(Context context, Locale desiredLocale) {
        Configuration conf = context.getResources().getConfiguration();
        conf = new Configuration(conf);
        conf.setLocale(desiredLocale);
        Context localizedContext = context.createConfigurationContext(conf);
        return localizedContext.getResources();
    }
